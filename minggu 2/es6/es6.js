const pertambahan = (num1, num2) => {
    return num1 + num2;
}

const perkalian = (num1, num2) => {
    return num1 * num2;
}

const pembagian = (num1, num2) => {
    return num1 / num2;
}

const luasAlasSegitiga = (a, t) => {
    return 0.5 * a * t;
}

const luasLingkaran = (d) => {
    return 3.14 * d;
}
